// Validators
import { IsString, IsOptional } from 'class-validator';

export class UpdateActivityFeedQueryParams {
  @IsString()
  @IsOptional()
  includeValues: string;

  @IsString()
  @IsOptional()
  selectValues: string;

  @IsString()
  @IsOptional()
  chosenOptionType: 'include' | 'select';
}
